                                                        zullen wij contact opnemen met de aanbieder aangezien zij
                                                        verantwoordelijk zijn voor de oplossing van de klacht. Indien de
                                                        consument liever zelf contact wil opnemen met de aanbieder, kan men
                                                        bij ons de gegevens van de desbetreffende aanbieder opvragen.
                                                        Gegevens worden alleen verstrekt indien de consument een veiling van
                                                        de desbetreffende aanbieder heeft gewonnen.</P>
