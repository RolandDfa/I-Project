                                                  <H3>Artikel 7. Vragen of niet tevreden?</H3>
                                                  <OL>
                                                    <LI><P>Indien er nog vragen zijn die
                                                      niet beantwoord worden onder het kopje ‘Vragen’ op de website
                                                      en/of na het lezen van de Algemene Voorwaarden, dan kan de consument
                                                      onze Klantenservice benaderen via de chat, het contactformulier via
