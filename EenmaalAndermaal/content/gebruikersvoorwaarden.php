  <H2>Artikel 1 Definities</H2>
  <P>
  </P>
  <H3>EenmaalAndermaal.nl</H3>
  <P>EenmaalAndermaal.nl is een
    veilingsite van iConcepts. EenmaalAndermaal.nl is een website waarmee
    bedrijven of ondernemingen producten of andere items ter veiling
    kunnen stellen aan andere bedrijven of ondernemingen.
  </P>
  <H3>Consument</H3>
  <P>Een persoon die handelt in de
    uitoefening van een bedrijf of andere instantie.</P>
    <H3>Bieder</H3>
    <P>De consument die zich op
      EenmaalAndermaal.nl geregistreerd heeft om deel te nemen aan een
      veiling.</P>
      <H3>Verkoper/ Aanbieder</H3>
      <P>De persoon of rechtspersoon die aan
        EenmaalAndermaal.nl opdracht heeft gegeven tot het in naam van en
        voor rekening van verkoper het te verkopen van één of meerdere
        zaken middels een veiling.</P>
        <H2>Artikel 2. Gebruiksvoorwaarden voor de
          veilingdiensten en registratie</H2>
          <OL>
            <LI><P>De consument is
              verantwoordelijk dat er bij registratie van zijn/haar gegevens bij
              EenmaalAndermaal.nl sprake is van authenticiteit.</P>
              <LI><P>Foutieve gegevens dienen direct
                door de consument gewijzigd te worden in zijn/haar
                gebruikersaccount.</P>
                <LI><P>De consument is te allen tijde
                  verantwoordelijk voor zijn/haar gebruikersaccount en inloggegevens.</P>
                  <LI><P>De consument dient deze
                    gegevens zorgvuldig voor zichzelf te houden en deze gegevens in geen
                    enkel geval door te geven aan derden.</P>
                    <LI><P>De consument is zelf
                      verantwoordelijk voor het gebruik van zijn/haar account en de
                      biedingen die vanaf dit account gedaan worden. Een winnend bod vanaf
                      het gebruikersaccount is bindend, ook indien een ander vanaf dit
                      account heeft geboden dan de consument die aan het gebruikersaccount
                      gekoppeld is. De gekoppelde consument is in dat geval verplicht de
                      betaling van de door hem/haar gewonnen veiling te verrichten.
                    </P>
                  </OL>
                  <H2> Artikel 3. Privacy beleid</H2>
                  <OL>
                    <LI><P>Geen enkele persoonsinformatie
                      die wordt verkregen zoals adres, email, kvk-nummer en bankgegevens
                      wordt door EenmaalAndermaal.nl aan derden verstrekt zonder
                      toestemming van de consument/bieder. Behalve, indien er voor zover
                      dit noodzakelijk is, met het oog op de uitvoering van de
                      overeenkomst en/of zoals dit artikel nader wordt omschreven.</P>
                      <LI><P>De adresgegevens uit het
                        gebruikersaccount wordt door EenmaalAndermaal.nl verstrekt aan de
                        aanbieder of aan het distributiecentrum die verantwoordelijk is voor
                        het verzenden van de gewonnen goederen aan de consument.</P>
                        <LI><P>Het e-mailadres uit het
                          gebruikersaccount wordt door EenmaalAndermaal.nl gebruikt om de
                          consument te informeren over de gewonnen veiling. Daarnaast zal de
                          consument geïnformeerd worden over eventuele nieuwe veilingen of
                          andere aanbiedingen. Dit kan door de consument op ieder moment
                          aangepast worden in zijn/haar gebruikersaccount.</P>
                        </OL>
                        <H2>Artikel 4. Betaling</H2>
                        <OL>
                          <LI><P>De betaling van de geveilde
                            producten zal via de Aanbieder lopen. De bieder ontvangt daarnaast
                            een bevestigingsmail nadat de hoogste bieding is uitgebracht op de
                            geveilde producten en de betreffende veiling is gesloten.</P>
                            <LI><P>Indien de consument het aan de
                              Aanbieder verschuldigde bedrag niet binnen 14 dagen na het winnen
                              van de veiling betaalt, dan heeft de Aanbieder het recht om
                              EenmaalAndermaal.nl hierover te informeren. EenmaalAndermaal.nl
                              onderneemt hierbij eigen sancties*.</P>
                            </OL>
                            <P>* Als de
                              consument na 14 dagen het bedrag niet betaalt dan zal
                              EenmaalAndermaal mailcontact op nemen met de consument. Als de
                              consument hier na 7 dagen geen reactie op geeft dan zal
                              EenmaalAndermaal overgaan op verdere acties. Onder deze acties
                              vallen: het recht van bieden te ontnemen van de consument of het
                              blokkeren van het gebruikersaccount van de consument.<BR>Neemt de
                                consument na deze stappen alsnog contact op met EenmaalAndermaal en
                                wil dat het gebruikersaccount gereactiveerd wordt, dan zal de
                                consument een boete moeten betalen, een deel van dat bedrag zal naar
                                de Aanbieder van de veiling gaan om gebruiksongemakken vergoed te
                                krijgen.
                              </P>
                              <H2>Artikel 5. Bieden</H2>
                              <OL>
                                <LI><P>De bieders mogen de prijs van
                                  een veiling niet (laten) manipuleren noch daar pogingen daartoe
                                  doen. Het gebruik van Biedrobots en het op die wijzen uitbrengen van
                                  automatisch biedingen is niet toegestaan.</P>
                                  <LI><P>Een bieder aanvaard de
                                    bijzondere omstandigheden die bij een online veiling kunnen spelen
                                    en de technische onvolkomenheden die zich daarbij kunnen voordoen.</P>
                                    <LI><P>EenmaalAndermaal.nl handelt
                                      slechts als bemiddelaar en niet in naam en voor rekening van de
                                      verkoper. Tussen de verkoper en de bieder komt een koopovereenkomst
                                      tot stand.
                                    </P>
                                  </OL>
